def categorize(name):
    n = name.lower()
    for ap in ("’", "'", "ʼ", "`", "´", "‛"):
        n = n.replace(ap, "")
    n = " " + n + " "
    if n.strip().startswith("пакет"):          # plastic bags
        return "household"
    if n.strip().startswith(("соус", "кетчуп")) or "заправк" in n:
        return "grocery"
    for key, _lbl, _emo, kws in CATS:
        for kw in kws:
            if kw in n:
                return key
    return "other"
